package edu.itdc.training.model;

public class City {
	private String city;
	private int cityId;
	
	public City () {}

	public City(String city) {
		this.city = city;
	}

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}
	
	public int getCityId() {
		return cityId;
	}
	
	
}
